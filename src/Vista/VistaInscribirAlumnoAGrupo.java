    private void cargarCombosBox(){
        ArrayList<GrupoEscolar> listaGrupos;
        
        listaGrupos = AdministradorDeGrupo.ObtenerLista();
        
        for(int i=0; i<listaGrupos.size(); i++){
            comboAsignatura.addItem(((GrupoEscolar)listaGrupos.get(i)));
        }
        
    }

